/**
 * Muestra los números del 320 al 160, contando de 20 en 20 hacia atrás utilizando un bucle for
 * 
 * 
 * @author Ale Cueto
 */
public class Tema05Ejercicio04AleCueto {
  public static void main(String[] args) {
    System.out.println("¡Vamos a sacar los números del 320 al 160 contando en 20 hacia atrás usando for!\n");
    
    
    for(int i = 320; i >= 160; i -= 20 ){
      System.out.println(i);
    }
  }
}
