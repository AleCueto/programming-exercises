public class Tema02Ejercicio01AleCueto {
  public static void main(String[] args) {
    int x = 144;
    int y = 999;
    System.out.println("x es igual a " + x + ", y es igual a " + y);
    System.out.println("la suma de x e y es igual a " + (x + y));
    System.out.println("la resta de x e y es igual a " + (x - y));
    System.out.println("la división de x e y es igual a " + ((float)x / (float)y));
    System.out.println("la multiplicación de x e y es igual a " + (x * y));
  }
}


